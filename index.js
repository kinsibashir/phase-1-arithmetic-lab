
// Write your code here
// Variables for multiplication
let num1 = 31;
let num2 = 2;
let multiply = num1 * num2; // Result will be 62

// Random integer greater than 0
let random = Math.floor(Math.random() * 100) + 1; // Generates between 1 and 100

// Variables for modulus
let num3 = 28;
let num4 = 6;
let mod = num3 % num4; // Result will be 4

// Max value in a set
let max = Math.max(5, 10, 15, 20); // Result will be 20

// Output check
console.log("multiply:", multiply); // 62
console.log("random:", random);     // Random > 0
console.log("mod:", mod);           // 4
console.log("max:", max);           // 20